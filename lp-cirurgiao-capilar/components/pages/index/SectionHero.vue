<script setup>
import gsap from 'gsap'
import { ScrollTrigger } from 'gsap/ScrollTrigger'

const landingRoot = ref(null)
let ctx

onMounted(() => {
  if (!landingRoot.value) return

  gsap.registerPlugin(ScrollTrigger)

  ctx = gsap.context(() => {
    gsap.from('.pill', {
      y: 20,
      opacity: 0,
      duration: 0.6,
      ease: 'power2.out'
    })

    gsap.from('.hero-texto h1', {
      y: 36,
      opacity: 0,
      duration: 0.85,
      delay: 0.1,
      ease: 'power3.out'
    })

    gsap.from('.descricao, .acoes a, .trust-bar span', {
      y: 24,
      opacity: 0,
      duration: 0.65,
      stagger: 0.1,
      delay: 0.22,
      ease: 'power2.out'
    })

    gsap.from('.card-base, .hero-selos-mobile .float-card', {
      y: 26,
      opacity: 0,
      duration: 0.7,
      delay: 0.3,
      ease: 'power2.out'
    })

    gsap.utils.toArray('.reveal-section').forEach((section) => {
      const targets = section.querySelectorAll('.bloco-head, .cards-diferenciais article, .texto-coluna > *, .timeline article, .depoimentos-grid article, .cta-texto, .cta-card')

      if (!targets.length) return

      gsap.from(targets, {
        y: 34,
        opacity: 0,
        duration: 0.8,
        stagger: 0.1,
        ease: 'power2.out',
        scrollTrigger: {
          trigger: section,
          start: 'top 78%',
          once: true
        }
      })
    })
  }, landingRoot.value)
})

onBeforeUnmount(() => {
  ctx?.revert()
})
</script>

